from flask import session


def create_message(color, messages):
    for i in range(0, messages.length):
        session['message_' + str(i)] = {
                                        'title': messages[i]['title'],
                                        'content': messages[i]['content'],
                                        'color': color[messages[i]['color']],
                                        'delete': bool(messages[i]['delete']),
                                        'container': bool(messages[i]['container'])
                                        }

    session['nbMessage'] = messages.length


def get_message():
    messages = []
    if 'nbMessage' in session and session['nbMessage'] > 0:
        for i in range(0, session['nbMessage']):
            messages.append(session['message_' + str(i)])
            session.pop('message_' + str(i), None)

        session.pop('nbMessage', None)

    return messages
